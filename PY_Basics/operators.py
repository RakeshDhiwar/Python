# Arithmetic operators
print(2 + 3) # Addition 
print(3 - 1) # Subtraction
print(2 * 3) # Multiplication
print(3 / 2) # Division
print(3 ** 2) # Exponentiation
print(5 % 2) # Modulus

# Comparison operators
print(3 > 2) # True
print(2 < 4) # True  
print(4 >= 4) # True
print(3 <= 3) # True
print(2 == 2) # True
print(2 != 3) # True

# Logical operators 
print(True and False) # Logical AND
print(True or False)  # Logical OR
print(not True) # Logical NOT

# Bitwise operators
print(2 & 3) # Bitwise AND 
print(2 | 3) # Bitwise OR
print(2 ^ 3) # Bitwise XOR
print(~2) # Bitwise NOT
print(2 << 3) # Bitwise left shift
print(3 >> 2) # Bitwise right shift

# Identity operators
a = 2
b = 2
print(a is b) # True (same object) 
print(a is not b) # False

# Membership operators 
numbers = [1, 2, 3]
print(1 in numbers) # True
print(4 not in numbers) # True